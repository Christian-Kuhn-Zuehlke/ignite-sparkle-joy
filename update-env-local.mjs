#!/usr/bin/env node
/**
 * Create/update .env.local with local Supabase development values
 * Run this after `supabase start` to configure local development
 * Works on Windows, Mac, and Linux
 */

import { execSync } from 'child_process';
import { writeFileSync } from 'fs';

console.log('🔧 Fetching local Supabase configuration...\n');

try {
  // Get Supabase status as JSON
  const output = execSync('supabase status --output json', { 
    encoding: 'utf-8',
    stdio: ['pipe', 'pipe', 'pipe']
  });
  
  const status = JSON.parse(output);
  
  // Create .env.local content
  const content = `# ===========================================
# Local Development Overrides
# ===========================================
# This file is automatically generated by update-env-local script
# It overrides .env for local development
# It's gitignored and should NEVER be committed
# ===========================================

# Local Supabase Development
VITE_SUPABASE_URL="${status.API_URL}"
VITE_SUPABASE_PUBLISHABLE_KEY="${status.ANON_KEY}"

# Service Role Key - ONLY for local development testing
# NEVER use this in production or commit it!
SUPABASE_SERVICE_ROLE_KEY="${status.SERVICE_ROLE_KEY}"

# Local Database URL (for admin/migrations)
DATABASE_URL="${status.DB_URL}"
`;

  writeFileSync('.env.local', content, 'utf-8');
  
  console.log('✅ Successfully created .env.local for local development\n');
  console.log(`   API URL: ${status.API_URL}`);
  console.log(`   Studio: ${status.STUDIO_URL}\n`);
  console.log('📝 Your production values in .env remain unchanged');
  console.log('🔒 .env.local is gitignored and won\'t be committed\n');
  
} catch (error) {
  console.error('❌ Error:', error.message);
  console.error('\nMake sure Supabase is running: supabase start');
  process.exit(1);
}
